# Também chamadas de 'laços de repetição', determinam a realização de uma tarefa repetidamente

# WHILE
# repete uma tarefa até que uma condição X passe a ser verdadeira

contador = 1

while contador <= 5:
    print(contador)
    contador += 1
print("\n")

# FOR
# repete uma tarefa até que uma sequência seja concluída

for i in range(0, 6):
    print(i)